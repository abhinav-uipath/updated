package com.demo;

public class ProductOfTwoFloatingNumber {
	public static void main(String args[]) {

		// local variable
		float number1 = 153.45f; // declared variable number1
		System.out.println("Number1:  " + number1);// print variable number1

		float number2 = 8.1f;// declared variable number2
		System.out.println("Number2:  " + number2);// print variable number2

		// temp variable
		float temp = number1 * number2;// declared temp variable

		// print temp variable
		System.out.println("Product of both Number1 & Number2:  " + temp);
	}

}
