package com.demo;

public class AddTwoInteger {

	public static void main(String agrs[]) {
		// local variable
		int number1 = 5;// declared variable number1
		System.out.println("Number1:  " + number1);// print variable number1

		int number2 = 6;// declared variable number2
		System.out.println("Number2:  " + number2);// print variable number2

		// temp variable
		int temp = number1 + number2; //Declared Temp Variable

		// print temp variable
		System.out.println("Sum of both Number1 & Number2:  " + temp);
	}

}
