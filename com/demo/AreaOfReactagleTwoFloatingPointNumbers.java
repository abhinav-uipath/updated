package com.demo;

public class AreaOfReactagleTwoFloatingPointNumbers {

	public static void main(String args[]) {

		// local variable
		float length = 15.45f; // declared variable length
		System.out.println("Number1:  " + length+" cm");// print variable length

		float breadth = 9.1f;// declared variable breadth
		System.out.println("Number2:  " + breadth+" cm");// print variable breadth

		// AreaOfReactagle
		float AreaOfReactagle = length * breadth;// declared AreaOfReactagle variable

		// print AreaOfReactagle variable
		System.out.println("Product of both Number1 & Number2:  " + AreaOfReactagle+" cm");
	}
}
