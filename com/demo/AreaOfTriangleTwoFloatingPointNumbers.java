package com.demo;

public class AreaOfTriangleTwoFloatingPointNumbers {

	public static void main(String args[]) {

		// local variable
		float height = 30.45f; // declared variable height
		System.out.println("Height:  " + height + " cm");// print variable height

		float base = 45.45f; // declared variable base
		System.out.println("Base:  " + base + " cm");// print variable base

		// AreaOfReactagle
		float AreaOfTriangle = (height * base) / 2;// declared AreaOfSquare variable

		// print AreaOfReactagle variable
		System.out.println("Product of both Side:  " + AreaOfTriangle + " cm");
	}
}